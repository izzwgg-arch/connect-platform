/**
 * Billing report helpers — read-only SELECT queries + CSV serialisation.
 *
 * All functions accept an injected `db` so they are unit-testable without a
 * live Postgres connection (same pattern as adminCardSave.ts).
 *
 * Safety:
 *  - Row caps prevent runaway queries on large datasets.
 *  - CSV cells are sanitised against formula-injection (cells that start with
 *    =, +, -, @, TAB, or CR are prefixed with a single-quote).
 *  - The CSV body is returned as a plain string; callers set the Fastify reply
 *    headers so nothing here touches HTTP concerns.
 */

// ── Row-cap constants ─────────────────────────────────────────────────────────

export const AGING_ROW_CAP = 2_000;
export const FAILED_PAYMENTS_ROW_CAP = 1_000;
export const INVOICE_EXPORT_ROW_CAP = 5_000;
export const TX_EXPORT_ROW_CAP = 5_000;

