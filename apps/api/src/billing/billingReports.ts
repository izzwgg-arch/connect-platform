// ── Days-overdue helper ───────────────────────────────────────────────────────

export function computeDaysOverdue(dueDate: Date | null, status: string, now: Date = new Date()): number {
  if (!dueDate) return 0;
  if (status === "PAID" || status === "VOID" || status === "DRAFT") return 0;
  const ms = now.getTime() - dueDate.getTime();
  return ms > 0 ? Math.floor(ms / (1000 * 60 * 60 * 24)) : 0;
}

// ── CSV cell sanitisation ─────────────────────────────────────────────────────

/**
 * Sanitise a single CSV cell value:
 *  - Converts to string (null/undefined → "")
 *  - Prefixes formula-injection starters (= + - @ TAB CR) with a single-quote
 *  - Wraps in double-quotes if the value contains commas, double-quotes, or newlines
 *  - Escapes embedded double-quotes by doubling them
 */
export function csvCell(value: unknown): string {
  if (value === null || value === undefined) return "";
  const s = String(value);
  // CSV injection defence: prefix with single-quote when the cell would be
  // interpreted as a formula by Excel / Sheets.
  const FORMULA_STARTERS = /^[=+\-@\t\r]/;
  const safe = FORMULA_STARTERS.test(s) ? `'${s}` : s;
  // If the value needs quoting, wrap in double-quotes and escape internal ones
  if (safe.includes(",") || safe.includes('"') || safe.includes("\n") || safe.includes("\r")) {
    return `"${safe.replace(/"/g, '""')}"`;
  }
  return safe;
}

export function csvRow(cells: unknown[]): string {
  return cells.map(csvCell).join(",");
}

// ── Date formatting ───────────────────────────────────────────────────────────

export function fmtIso(d: Date | null | undefined): string {
  if (!d) return "";
  return d instanceof Date ? d.toISOString().slice(0, 10) : String(d).slice(0, 10);
}

export function fmtDatetime(d: Date | null | undefined): string {
  if (!d) return "";
  return d instanceof Date ? d.toISOString().replace("T", " ").slice(0, 19) + " UTC" : String(d);
}

function centsToDollars(cents: number): string {
  return (cents / 100).toFixed(2);
}

// ── CSV header metadata rows ──────────────────────────────────────────────────

export function csvMeta(reportName: string, generatedBy: string): string {
  const now = new Date();
  return [
    csvRow(["# Report", reportName]),
    csvRow(["# Generated At", fmtDatetime(now)]),
    csvRow(["# Generated By", generatedBy]),
    "",
  ].join("\n");
}

// ── Today's date for filename suffix ─────────────────────────────────────────

export function todayDateSuffix(): string {
  return new Date().toISOString().slice(0, 10); // YYYY-MM-DD
}

// ── Aging report ─────────────────────────────────────────────────────────────

export type BillingReportScope = { tenantId?: string };

export async function queryAgingReport(
  reportDb: ReportDb,
  scope: BillingReportScope = {},
): Promise<{ rows: AgingRow[]; capped: boolean }> {
  const raw = await reportDb.billingInvoice.findMany({
    where: {
      status: { in: ["OPEN", "FAILED", "OVERDUE"] },
      balanceDueCents: { gt: 0 },
      ...(scope.tenantId ? { tenantId: scope.tenantId } : {}),
    },
    orderBy: [{ dueDate: "asc" }],
    take: AGING_ROW_CAP + 1,
    select: {
      id: true,
      invoiceNumber: true,
      tenantId: true,
      status: true,
      dueDate: true,
      balanceDueCents: true,
      totalCents: true,
      createdAt: true,
      tenant: { select: { name: true } },
    },
  }) as any[];

  const capped = raw.length > AGING_ROW_CAP;
  const slice = raw.slice(0, AGING_ROW_CAP);

  const now = new Date();
  const rows: AgingRow[] = slice.map((r: any) => ({
    invoiceId: r.id,
    invoiceNumber: r.invoiceNumber,
    tenantId: r.tenantId,
    tenantName: r.tenant?.name ?? r.tenantId,
    status: r.status,
    dueDate: r.dueDate ? new Date(r.dueDate) : null,
    daysOverdue: computeDaysOverdue(r.dueDate ? new Date(r.dueDate) : null, r.status, now),
    balanceDueCents: r.balanceDueCents,
    totalCents: r.totalCents,
    createdAt: new Date(r.createdAt),
  }));

  return { rows, capped };
}

export function agingToCsv(rows: AgingRow[], meta: string): string {
  const header = csvRow(["Tenant", "Invoice #", "Status", "Due Date", "Days Overdue", "Balance Due", "Total"]);
  const dataRows = rows.map((r) =>
    csvRow([
      r.tenantName,
      r.invoiceNumber ?? "",
      r.status,
      fmtIso(r.dueDate),
      r.daysOverdue,
      centsToDollars(r.balanceDueCents),
      centsToDollars(r.totalCents),
    ]),
  );
  return [meta, header, ...dataRows].join("\n");
}

// ── Failed payments report ────────────────────────────────────────────────────

export async function queryFailedPaymentsReport(
  reportDb: ReportDb,
  scope: BillingReportScope = {},
): Promise<{ rows: FailedPaymentRow[]; capped: boolean }> {
  const raw = await reportDb.billingInvoice.findMany({
    where: {
      status: { in: ["FAILED", "OVERDUE"] },
      ...(scope.tenantId ? { tenantId: scope.tenantId } : {}),
    },
    orderBy: [{ failedAt: "desc" }, { dueDate: "asc" }],
    take: FAILED_PAYMENTS_ROW_CAP + 1,
    select: {
      id: true,
      invoiceNumber: true,
      tenantId: true,
      status: true,
      totalCents: true,
      balanceDueCents: true,
      failedAt: true,
      tenant: { select: { name: true } },
      transactions: {
        where: { status: { in: ["DECLINED", "ERROR"] } },
        orderBy: { createdAt: "desc" },
        take: 1,
        select: { responseMessage: true, responseCode: true, createdAt: true },
      },
    },
  }) as any[];

  const capped = raw.length > FAILED_PAYMENTS_ROW_CAP;
  const slice = raw.slice(0, FAILED_PAYMENTS_ROW_CAP);

  const rows: FailedPaymentRow[] = slice.map((r: any) => {
    const lastTx = r.transactions?.[0] ?? null;
    return {
      invoiceId: r.id,
      invoiceNumber: r.invoiceNumber,
      tenantId: r.tenantId,
      tenantName: r.tenant?.name ?? r.tenantId,
      totalCents: r.totalCents,
      balanceDueCents: r.balanceDueCents,
      status: r.status,
      lastFailureReason: lastTx?.responseMessage ?? null,
      lastResponseCode: lastTx?.responseCode ?? null,
      lastAttemptAt: lastTx?.createdAt ? new Date(lastTx.createdAt) : null,
      failedAt: r.failedAt ? new Date(r.failedAt) : null,
    };
  });

  return { rows, capped };
}

export function failedPaymentsToCsv(rows: FailedPaymentRow[], meta: string): string {
  const header = csvRow(["Tenant", "Invoice #", "Status", "Total", "Balance Due", "Last Failure Reason", "Response Code", "Last Attempt", "Failed At"]);
  const dataRows = rows.map((r) =>
    csvRow([
      r.tenantName,
      r.invoiceNumber ?? "",
      r.status,
      centsToDollars(r.totalCents),
      centsToDollars(r.balanceDueCents),
      r.lastFailureReason ?? "",
      r.lastResponseCode ?? "",
      fmtDatetime(r.lastAttemptAt),
      fmtDatetime(r.failedAt),
    ]),
  );
  return [meta, header, ...dataRows].join("\n");
}

